/**
 * Função para obter a tradução de um mercado
 * @param {string} marketCode - Código do mercado
 * @returns {string} - Tradução do mercado ou o código original se não encontrado
 */
export function getMarketTranslation(marketCode) {
  if (!marketCode) return 'Resultado Final'
  return marketTranslations[marketCode] || marketCode
}

/**
 * Função para extrair o código do mercado de uma string completa
 * @param {string} marketString - String completa do mercado
 * @returns {string|null} - Código extraído ou null se não encontrado
 */
export function extractMarketCode(marketString) {
  if (!marketString) return null
  
  // Ordena os códigos por tamanho (maior primeiro) para evitar matches parciais
  const sortedCodes = Object.keys(marketTranslations).sort((a, b) => b.length - a.length)
  
  // Procura por códigos conhecidos no início da string
  for (const code of sortedCodes) {
    if (marketString.startsWith(code)) {
      return code
    }
  }
  
  // Procura por códigos seguidos de parênteses, espaços ou outros separadores
  for (const code of sortedCodes) {
    const regex = new RegExp(`\\b${code.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}\\b`)
    if (regex.test(marketString)) {
      return code
    }
  }
  
  // Procura por códigos no meio da string (fallback)
  for (const code of sortedCodes) {
    if (marketString.includes(code)) {
      return code
    }
  }
  
  return null
}

/**
 * Função para obter a tradução completa de um mercado
 * @param {string} marketString - String completa do mercado
 * @returns {string} - Mercado com tradução aplicada
 */
export function translateMarket(marketString) {
  if (!marketString) return 'Resultado Final'
  
  const marketCode = extractMarketCode(marketString)
  if (!marketCode) return marketString
  
  const translation = getMarketTranslation(marketCode)
  return marketString.replace(marketCode, translation)
}

export default {
  marketTranslations,
  getMarketTranslation,
  extractMarketCode,
  translateMarket
}
