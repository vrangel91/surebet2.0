/**
 * Constr√≥i a URL completa para uma casa de apostas baseada no dom√≠nio extra√≠do
 * @param {string} domain - Dom√≠nio extra√≠do do anchorh
 * @param {boolean} isLive - Se √© um jogo live
 * @returns {string} URL completa para a casa de apostas
 */
export function buildBookmakerUrlFromDomain(domain, isLive = false) {
  if (!domain) {
    return null;
  }

  // Adiciona protocolo se n√£o tiver
  let url = domain.startsWith("http") ? domain : `https://${domain}`;

  // Se √© live, tenta adicionar par√¢metros espec√≠ficos para jogos ao vivo
  if (isLive) {
    // Adiciona par√¢metros comuns para jogos live
    const liveParams = ["/live", "/ao-vivo", "/in-play", "/live-betting"];
    const hasLiveParam = liveParams.some((param) => url.includes(param));

    if (!hasLiveParam) {
      // Adiciona o primeiro par√¢metro live dispon√≠vel
      url = url.endsWith("/") ? url + "live" : url + "/live";
    }
  }

  console.log(`üèóÔ∏è URL constru√≠da para dom√≠nio ${domain}:`, url);
  return url;
}
